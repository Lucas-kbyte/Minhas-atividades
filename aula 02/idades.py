idade = input("Qual é a sua idade?")

if idade <= "13":
     
     print("Você é uma criaça")
elif idade >= "13" and idade <= "17":
     
     print("Você é um adolescente")
elif idade  >= "18" and idade <= "59":

      print("Você é um adulto")
elif idade >= "60":

      print("Você é uma mumia") 